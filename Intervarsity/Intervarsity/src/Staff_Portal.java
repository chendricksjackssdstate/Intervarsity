    //Deletes the selected user account;
    //Administrators cannot delete their own account
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(staff.fullname.equals(jComboBox1.getSelectedItem().toString())){
            JOptionPane.showMessageDialog(null, "You cannot delete your account. Please see another administrator to delete your account");
        }
        else{
            StaffList staff = new StaffList();
            if(JOptionPane.showConfirmDialog(null, jComboBox1.getSelectedItem().toString() + " is about to be deleted. Are you sure you want to delete him") == JOptionPane.YES_OPTION){
                staff.setDelete(jComboBox1.getSelectedItem().toString());
                JOptionPane.showMessageDialog(null, jComboBox1.getSelectedItem().toString() + " was successfully deleted.");
                jCheckBox1.setSelected(false);
                jRadioButton1.setSelected(false);
                jRadioButton2.setSelected(false);
                jRadioButton3.setSelected(false);
                jRadioButton4.setSelected(false);
                load = false;
                StaffList staff1 = new StaffList();
                String[] staffarray = new String[staff1.getStaffCount()];
                if(school == University.ALL)
                    staffarray = staff1.getStafflist();
                else
                    staffarray = staff1.getStafflist(school);
                jComboBox1.removeAllItems();
                for(int i = 0; i < staffarray.length; i++){
                    jComboBox1.addItem(staffarray[i]);
                }
                load = true;
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

