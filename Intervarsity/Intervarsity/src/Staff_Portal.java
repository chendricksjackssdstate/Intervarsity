    //changes the user information shown when new user is selected
    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if(load){
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(false);
            jRadioButton3.setSelected(false);
            jRadioButton4.setSelected(false);
            StaffList staff = new StaffList();
            node = staff.getName(jComboBox1.getSelectedItem().toString());
            jCheckBox1.setSelected(node.admin);
            if(node.school == University.ALL)
                jRadioButton1.setSelected(true);
            else if(node.school == University.SOUTHDAKOTASTATE)
                jRadioButton2.setSelected(true);
            else if(node.school == University.NORTHERNSTATE)
                jRadioButton3.setSelected(true);
            else
                jRadioButton4.setSelected(true);
        }
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

    //updates the selected user account
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        StaffList staff = new StaffList();
        node.admin = jCheckBox1.isSelected();
        if(jRadioButton1.isSelected())
            node.school = University.ALL;
        else if(jRadioButton2.isSelected())
            node.school = University.SOUTHDAKOTASTATE;
        else if(jRadioButton3.isSelected())
            node.school = University.NORTHERNSTATE;
        else
            node.school = University.DAKOTASTATE;
        staff.update(node);
        while(!staff.savelist());
        JOptionPane.showMessageDialog(null, node.fullname + " was successfully updated");
    }//GEN-LAST:event_jButton4ActionPerformed

