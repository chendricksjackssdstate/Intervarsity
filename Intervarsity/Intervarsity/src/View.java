    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        student.name = jTextField1.getText();
        student.biblestudy = jCheckBox1.isSelected();
        student.meetpeople = jCheckBox2.isSelected();
        student.talkjesus = jCheckBox3.isSelected();
        student.Findchurch = jCheckBox4.isSelected();
        student.Learniv = jCheckBox5.isSelected();
        student.funevents = jCheckBox6.isSelected();
        student.nothing = jCheckBox7.isSelected();
        student.other = jCheckBox8.isSelected();
        student.otherstring = jTextField2.getText();
        if(jRadioButton1.isSelected())
            student.studentyear = Year.FRESHMAN;
        else if(jRadioButton2.isSelected())
            student.studentyear = Year.SOPHOMORE;
        else if(jRadioButton3.isSelected())
            student.studentyear = Year.JUNIOR;
        else if(jRadioButton4.isSelected())
            student.studentyear = Year.SENIOR;
        else
            student.studentyear = Year.GRADSTUDENT;
        student.residencehall = jComboBox1.getSelectedItem().toString();
        student.offcampus = jRadioButton6.isSelected();
        student.phone = jTextField3.getText();
        student.email = jTextField4.getText();
        if(jRadioButton7.isSelected())
            student.studentcontact = PreferContact.PHONE;
        else if(jRadioButton8.isSelected())
            student.studentcontact = PreferContact.TEXT;
        else if(jRadioButton9.isSelected())
            student.studentcontact = PreferContact.EMAIL;
        else
            student.studentcontact = PreferContact.NEITHER;
        student.staffcontact = jComboBox2.getSelectedItem().toString();
        
        //Fill Date
        int month = jDateChooser1.getCalendar().get(MONTH) + 1;
        int day = jDateChooser1.getCalendar().get(DAY_OF_MONTH);
        int year = jDateChooser1.getCalendar().get(YEAR);
        student.dateadded = Integer.toString(month) + "-" + Integer.toString(day) + "-" + Integer.toString(year);
        
        
        student.semester = jComboBox3.getSelectedItem().toString();
        student.facetoface = jCheckBox9.isSelected();
        student.callemail = jCheckBox10.isSelected();
        student.twoxconnection = jCheckBox11.isSelected();
        student.threexconnection = jCheckBox12.isSelected();
        student.lginvite = jCheckBox13.isSelected();
        student.sginvite = jCheckBox14.isSelected();
        student.notes = jTextArea1.getText();
        StudentList list = new StudentList(originalsemester, school);
        list.update(student, originalname);
        while(!list.savelist());
        JOptionPane.showMessageDialog(null, jTextField1.getText() + " has been successfully updated");
    }//GEN-LAST:event_jButton4ActionPerformed
