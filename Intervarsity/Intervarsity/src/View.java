    //updates the view of the student information
    private void updateview(){
        String[] names;
        //Fill Residence Hall
        ResidenceHallList reslist = new ResidenceHallList(school);
        names = reslist.getHallList();
        
        jComboBox1.removeAllItems();
        jComboBox1.addItem("Select Residence Hall");
        for(int i = 0; i < names.length; i++){
            jComboBox1.addItem(names[i]);
        }
        
        //Fill Staff List
        names = null;
        StaffList stafflist = new StaffList();
        names = stafflist.getStafflist(school);
        
        jComboBox2.removeAllItems();
        jComboBox2.addItem("No Staff Assigned");
        for(int i = 0; i < names.length; i++){
            if(names[i] == null){
                
            }
            else{
                jComboBox2.addItem(names[i]);
            }
        }
        //Fill Semester List
        names = null;
        SemesterList semlist = new SemesterList(school);
        names = semlist.getSemesterNames();
        
        jComboBox3.removeAllItems();
        for(int i = 0; i < names.length; i++){
            jComboBox3.addItem(names[i]);
        }
        
        
        //Display Student Information
        jTextField1.setText(student.name);
        originalname = student.name;
        jCheckBox1.setSelected(student.biblestudy);
        jCheckBox2.setSelected(student.meetpeople);
        jCheckBox3.setSelected(student.talkjesus);
        jCheckBox4.setSelected(student.Findchurch);
        jCheckBox5.setSelected(student.Learniv);
        jCheckBox6.setSelected(student.funevents);
        jCheckBox7.setSelected(student.nothing);
        jCheckBox8.setSelected(student.other);
        jTextField2.setText(student.otherstring);
        if(student.studentyear == Year.FRESHMAN)
            jRadioButton1.setSelected(true);
        else if(student.studentyear == Year.SOPHOMORE)
            jRadioButton2.setSelected(true);
        else if(student.studentyear == Year.JUNIOR)
            jRadioButton3.setSelected(true);
        else if(student.studentyear == Year.SENIOR)
            jRadioButton4.setSelected(true);
        else
            jRadioButton5.setSelected(true);
        jComboBox1.setSelectedItem(student.residencehall);
        jRadioButton6.setSelected(student.offcampus);
        jTextField3.setText(student.phone);
        jTextField4.setText(student.email);
        if(student.studentcontact == PreferContact.PHONE)
            jRadioButton7.setSelected(true);
        else if(student.studentcontact == PreferContact.TEXT)
            jRadioButton8.setSelected(true);
        else if(student.studentcontact == PreferContact.EMAIL)
            jRadioButton9.setSelected(true);
        else
            jRadioButton10.setSelected(true);

        SimpleDateFormat formatter = new SimpleDateFormat("MM-dd-yy");
        try {
             Date dat = (Date)formatter.parse(student.dateadded);
            jDateChooser1.setDate(dat);
        } catch (ParseException ex) {
            Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
        }

        jComboBox2.setSelectedItem(student.staffcontact);
        jComboBox3.setSelectedItem(student.semester);
        originalsemester = student.semester;
        jCheckBox9.setSelected(student.facetoface);
        jCheckBox10.setSelected(student.callemail);
        jCheckBox11.setSelected(student.twoxconnection);
        jCheckBox12.setSelected(student.threexconnection);
        jCheckBox13.setSelected(student.lginvite);
        jCheckBox14.setSelected(student.sginvite);
        jTextArea1.setText(student.notes);
    }
    
