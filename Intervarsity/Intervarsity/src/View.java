    //deletes the student currently displayed
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(JOptionPane.showConfirmDialog(null, jTextField1.getText() + " is about to be deleted. Are you sure you want to delete them?") == JOptionPane.YES_OPTION){
            jComboBox3.getSelectedItem().toString();
            StudentList list = new StudentList(jComboBox3.getSelectedItem().toString(), school);
            list.setdelete(jTextField1.getText());
            while(!list.savelist());
            JOptionPane.showMessageDialog(null, "Successfully deleted " + jTextField1.getText());
            resetform();
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

